package tsetttt;

public class Student {
	
	private String name;
	private int age;
	String phone;
	int hight;
	
	public Student(String name, int age, String phone, int hight) {
		this.name = name;
		this.age = age;
		this.phone = phone;
		this.hight = hight;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}
	
	
	
	
	

}
